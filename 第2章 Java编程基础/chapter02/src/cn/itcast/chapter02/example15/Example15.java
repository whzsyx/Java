package cn.itcast.chapter02.example15;
/**
 * for循环(使用*打印直角三角形)
 */
public class Example15 {
	public static void main(String[] args) {
		int i, j; // 定义两个循环变量
		for (i = 1; i <= 9; i++) { // 外层循环
			for (j = 1; j <= i; j++) { // 内层循环
				System.out.print("*"); // 打印*
			}
			System.out.print("\n"); // 换行
		}
	}
}